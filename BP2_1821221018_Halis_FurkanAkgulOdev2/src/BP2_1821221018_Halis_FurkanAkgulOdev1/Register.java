    private void kaydolRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kaydolRActionPerformed
        String sifre= new String(jPasswordField1.getPassword());
        String c = "[0-9][0-9]";
        if (ad.getText().isEmpty() || soyad.getText().isEmpty() || kullanıcıAdı.getText().isEmpty() || sifre.isEmpty() || telefonNo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Lütfen bütün yerleri doldurunuz...", "Hatalı işlem", NORMAL);
        } else if (!desenkontrol(telefonNo.getText(), c)) {
            JOptionPane.showMessageDialog(this, "Telefon numaranızı kontrol ediniz...", "Eksik Veya Hatalı Bir İşlem Var", NORMAL);
        } else {
            EntityManager em = emf.createEntityManager();
            Query q = em.createQuery("select kk from Ekullanici kk");
            List<Ekullanici> ekullanicilar = q.getResultList();
            em.getTransaction().begin();
            boolean kontrol = true;
            Ekullanici k = new Ekullanici();
            k.setId(personelDongu()+1);
            k.setAdi(ad.getText());
            k.setSoyadi(soyad.getText());
            k.setKullaniciAdi(kullanıcıAdı.getText());
            k.setSiFre(sifre);
            k.setTelefon(Integer.parseInt(telefonNo.getText()));
            kontrol = true;
            for (Ekullanici ekullanici : ekullanicilar) {
                if((kullanıcıAdı.getText().equals(ekullanici.getKullaniciAdi()))){
                    kontrol=false;
                    break;
                }else{
                    kontrol=true;
                    em.persist(k);
                    em.getTransaction().commit();
                    JOptionPane.showMessageDialog(this, "Kullanıcı Başarılı Bir Şekilde Eklendi...");
                    Login login = new Login();
                    login.setVisible(true);
                    login.setLocationRelativeTo(null);
                    this.setVisible(false);
                    em.close();
                    break;
                }
            }
            if(!kontrol) {
                JOptionPane.showMessageDialog(this, "Bu Kullanıcı Adı Farklı Bir Kullanıcı Tarafından Kullanılıyor...", "Hatalı Veya Eksik İşlem.", NORMAL);
            }
           
        }
    }//GEN-LAST:event_kaydolRActionPerformed

