    int personelDongu() {
        EntityManager em = emf.createEntityManager();
        em.getTransaction().begin();
        Epersonel p = new Epersonel();
        Query q = em.createQuery("select k from Ekullanici k");
        List<Ekullanici> ekul = q.getResultList();
        int sonPer = 0;
        for (Ekullanici ekullanici : ekul) {
            if(ekullanici.getId()>sonPer){
                sonPer=ekullanici.getId();
            }
        }
        return sonPer;
    }

