    private void silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silActionPerformed
        toplamTutar -= adetler.get(siparisListesi.getSelectedIndex()) * siparis.get(siparisListesi.getSelectedIndex()).getFiyat();
        adetler.remove(siparisListesi.getSelectedIndex());
        siparis.remove(siparisListesi.getSelectedIndex());
        dlm.removeAllElements();
        for (Cicek c : siparis) {
            dlm.addElement(adetler.get(siparis.indexOf(c)) + " adet " + c.getRenk() + " " + c.getTür());
        }
        tutarEkran.setText(toplamTutar + " ");
        siparisListesi.setModel(dlm);
        sil.setEnabled(false);
    }//GEN-LAST:event_silActionPerformed

