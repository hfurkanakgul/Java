    public void yeniSiparis() {
        dlm.removeAllElements();
        for (Cicek c : siparis) {
            dlm.addElement(adetler.get(siparis.indexOf(c)) + " adet " + c.getRenk() + " " + c.getTür());
        }
        siparisListesi.setModel(dlm);
        tutarEkran.setText(toplamTutar + "");
    }


    private void hepsiniListeleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hepsiniListeleActionPerformed
        doldur();
        int a = dtm.getRowCount();
        for (int i = 0; i < a; i++) {
            dtm.removeRow(0);
        }
        for (Cicek cicek : cicekler) {
            if (cicek instanceof CansizCicek) {

                Object[] o = {((CansizCicek) cicek).getTür(), ((CansizCicek) cicek).getRenk(), ((CansizCicek) cicek).getFiyat(), ((CansizCicek) cicek).getKategori(), "Cansız", ((CansizCicek) cicek).getMalzeme()};
                dtm.addRow(o);
            } else {
                Object[] o = {cicek.getTür(), cicek.getRenk(), cicek.getFiyat(), cicek.getKategori(), "Canlı", "Doğal"};
                dtm.addRow(o);
            }

        }
        cicekTablosu.setModel(dtm);
        cicekler.removeAll(cicekler);
        ekle.setEnabled(false);
        sil.setEnabled(false);
    }//GEN-LAST:event_hepsiniListeleActionPerformed

    private void kategorilerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kategorilerActionPerformed

        doldur();
        int a = dtm.getRowCount();
        for (int i = 0; i < a; i++) {
            dtm.removeRow(0);
        }

        for (Cicek cicek : cicekler) {
            if (kategoriler.getSelectedItem().toString().equals(cicek.getKategori()) && !(cicek instanceof CansizCicek)) {
                Object[] o = {cicek.getTür(), cicek.getRenk(), cicek.getFiyat(), cicek.getKategori(), "Canlı", "Doğal"};
                dtm.addRow(o);

            } else if (kategoriler.getSelectedItem().toString().equals(cicek.getKategori()) && (cicek instanceof CansizCicek)) {
                Object[] o = {((CansizCicek) cicek).getTür(), ((CansizCicek) cicek).getRenk(), ((CansizCicek) cicek).getFiyat(), ((CansizCicek) cicek).getKategori(), "Cansız", ((CansizCicek) cicek).getMalzeme()};
                dtm.addRow(o);

            }
        }

        cicekler.removeAll(cicekler);
        ekle.setEnabled(false);
        sil.setEnabled(false);

    }//GEN-LAST:event_kategorilerActionPerformed
    

    private void ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekleActionPerformed

        adetler.add(adet.getSelectedIndex() + 1);
        dlm.removeAllElements();
        toplamTutar += Integer.parseInt(cicekTablosu.getValueAt(cicekTablosu.getSelectedRow(), 2).toString()) * (adet.getSelectedIndex() + 1);

        siparis.add(new Cicek(cicekTablosu.getValueAt(cicekTablosu.getSelectedRow(), 0).toString(), cicekTablosu.getValueAt(cicekTablosu.getSelectedRow(), 1).toString(), Integer.parseInt(cicekTablosu.getValueAt(cicekTablosu.getSelectedRow(), 2).toString()), cicekTablosu.getValueAt(cicekTablosu.getSelectedRow(), 3).toString()));
        for (Cicek c : siparis) {
            dlm.addElement(adetler.get(siparis.indexOf(c)) + " adet " + c.getRenk() + " " + c.getTür());
        }
        siparisListesi.setModel(dlm);

        tutarEkran.setText(toplamTutar + "");
        ekle.setEnabled(false);
    }//GEN-LAST:event_ekleActionPerformed
    

