    private void güncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_güncelleActionPerformed
        EntityManager em = emf.createEntityManager();
        em.getTransaction().begin();
        Query q = em.createQuery("UPDATE Epersonel p SET p.adi=:pAdı ,p.soyadi=:pSoyad ,p.maas=:pMaas , p.unvanAdi=:puID WHERE p.id=:pID");
        q.setParameter("pAdı", txt_adi.getText());
        q.setParameter("pSoyad", txt_soyadi.getText());
        q.setParameter("puID",cBoxUnvan.getSelectedItem());
        q.setParameter("pMaas", Integer.parseInt(txt_maas.getText()));
        q.setParameter("pID", Integer.parseInt((String)(dtm.getValueAt(jTable1.getSelectedRow(), 0))));
        q.executeUpdate();
        em.getTransaction().commit();
        JOptionPane.showMessageDialog(this, "Personel Başarılı Bir Şekilde Güncellendi...");
    }//GEN-LAST:event_güncelleActionPerformed

