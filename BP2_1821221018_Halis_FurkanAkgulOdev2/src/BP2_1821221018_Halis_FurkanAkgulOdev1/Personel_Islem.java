    private void SorgulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SorgulaActionPerformed
       EntityManager em = emf.createEntityManager();
       Query q = em.createQuery("select p from Epersonel p where p.unvanAdi=:PID");
       q.setParameter("PID",cBoxUnvan.getSelectedItem());
       List<Epersonel>personeller = q.getResultList();
        dtm.setRowCount(0);
        for (Epersonel epersonel : personeller) {
            dtm.addRow(new Object[]{epersonel.getId(), epersonel.getAdi(), epersonel.getSoyadi(),epersonel.getMaas(),epersonel.getUnvanAdi()});
        }
    }//GEN-LAST:event_SorgulaActionPerformed

